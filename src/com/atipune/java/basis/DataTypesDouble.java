package com.atipune.java.basis;

public class DataTypesDouble {

   
	double d1=23.6;
	double d2=-96545f;
	double d3=154.456666666666666d;
	double d4=23.6f;
	double d5=23.6d;
	double d6=1254.78644444444444444f;
	
	public void display()
	{
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(d3);
		System.out.println(d4);
		System.out.println(d5);
		System.out.println(d6);
	}
	
	public static void main(String[] args)
	{
		DataTypesDouble dtd= new DataTypesDouble();
		dtd.display();

	}

}
