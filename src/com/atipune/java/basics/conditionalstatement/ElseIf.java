package com.atipune.java.basics.conditionalstatement;

public class ElseIf {

	public static void main(String[] args) {
		
		int time=20;
		
		if(time < 15)
		{
			System.out.println("good morning");
		}
		else if(time<16)
		{
			System.out.println("good afternoon");
		}
		else
		{
			System.out.println("good night");
		}
			

	}

}
