package com.atipune.java.operators;

public class assignmentOperators {

	public static void main(String[] args) {
		
		int x=20;
		
		System.out.println(x);
		System.out.println(x+=5);
		System.out.println(x-=5);
		System.out.println(x*=5);
		System.out.println(x/=5);
		System.out.println(x%=5);
	}

}
