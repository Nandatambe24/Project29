package com.atipune.java.operators;

public class IncrementDecrement {

	public static void main(String[] args) {
	
		int i=15;
		
		System.out.println("preincrement value of i:"+ ++i);
		System.out.println(" value of i:"+i);
		System.out.println("postincrement value of i:"+ i++);
		System.out.println(" value of i:"+i);
		System.out.println("predecrement value of i:"+ --i);
		System.out.println(" value of i:"+i);
		System.out.println("postdecrement value of i:"+ i--);
		System.out.println(" value of i:"+i);
				
	}

}
