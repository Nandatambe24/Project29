package com.atipune.java.operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		
		int x=10,y=4;
		
		System.out.println("x>y:"+(x>y));
		System.out.println("x<y:"+(x<y));
		System.out.println("x>=y:"+(x>=y));
		System.out.println("x<=y:"+(x<=y));
		System.out.println("x==y:"+(x==y));
		System.out.println("x!=y:"+(x!=y));
	}

}
