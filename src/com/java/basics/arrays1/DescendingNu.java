package com.java.basics.arrays1;



public class DescendingNu {

	public static void main(String[] args) {
      int[] a= {100,45,23,75,80};
      for(int i=0;i<a.length;i++)
      {
    	  System.out.println(a[i]);
      }
      System.out.println("*******************");
     
      for(int i=4;i<=0;i--)
      {
    	  System.out.println(a[i]);
      }

	}

}
